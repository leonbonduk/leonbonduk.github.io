<dl>	 
<dt>Challenger bank</dt>	 
<dd>One of the new wave of start up companies providing banking services ! usually just over the internet or through an app on your phone. Butt here are one two smaller high street banks classed as such as well such as Metro Bank, TSB and Virgin.</dd>

<dt>Crypto</dt>	 
<dd>Short for cryptocurrency, an oline currency usually utilising blockchain technology to provide a method to transact whilst avoiding the usual banking institutions and their fees.</dd>	 
	
<dt>ETF</dt>	 
<dd>Exchange Traded Fund, usually a very low cost way of trading funds. Typically these are passive funds such that there is minimal input from a manager in determining what to invest in. Passive funds normally invest based on a pre determined methodology such as following market index. They can be a very useful investment to trade quickly and cheaply into a market trend or theme.</dd>
<dt>P2P</dt>	 
<dd>P2P is short for Person to Person lending. This is where any member of the public can lend their iwn money directly to another member of the public. The intermediary website takes a small fixed cut but the rest if the risk and reward resides with the person providing the funds. Therefore potentially this form of investment can provide better rates to both the lender and the holder of the loan.</dd> 
<dt>PE ratio (price to earnings ratio)</dt>	 
<dd>this a commonly used methodology to determine whether a company is under or overvalued. It is determined as the ratio of the share price of the company as a proportion of its earnings or profit after tax. The S&P 500 has typically traded in a range from 10 to 20. So typically a ratio under 10 could indicate that a company is undervalued but may also indicate that the company is going through a rough patch such that its results may come in lower than last time or it may be about to cut its dividend or make a cash call on investors. A value above 20 could indicate that a conpany is overvalued but high growth companies can typically hold such valuations for long periods of time also.</dd>

