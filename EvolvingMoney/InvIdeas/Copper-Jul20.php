		<h3>Leon, 5th July 2020, Wisdomtree Copper ETF (COPA), Price $24.62</h3>
		<p><img class="article-img-fit" src="https://www.evolving.money/Images/copperman_EM.jpg" alt="Metal statue of man with head in his hands" title="Metal man with man flu"> </p>
    <p>I completely misjudged the severity of the impact of covid-19 and so it’s impact on the world economy back at the end of January!</p>
    <p>On 28th Jan copper was trading at $2.58 /lb but although the price increased throughout February and early March it dramatically fell from 6th March as the world went into lockdown to a low on 23rd March of $2.10/lb (COPA $19.185)!</p>
    <p>From that date, however, the copper price has rocketed back, like a man recovering from a severe case of man flu and now stands at $2.71/ lb as of 3rd July, 5% above the price on the original mistimed article. This has probably been driven by several reasons I will briefly discuss…</p>
    <h2>Supply</h2>
    <p>Copper stocks measured by the Shanghai Futures exchange rose from 124k tonnes at the start of the year to 380k tonnes in March, although seasonally this happens over the Chinese new year, it will have also been significantly driven by the slow down caused by Covid-19 resulting in an oversupply of Copper during that period. Now however, stock levels are back down below the position at start of the year and now run at 114k tonnes as the Chinese economy has roared back to life.</p>
    <p>As discussed in the previous article, despite Chinese warehouses filling up throughout February and March due to factory output stalling under lockdown conditions, supply constraints from already depleting South American copper mines will have been exacerbated by fears of temporary reductions in supply in the short term caused by the pandemic ravaging through those countries. The pandemic will likely also restrict future supply due to probable delayed investment in new mining projects given the current uncertain situation.</p>
    <h2>Demand</h2>
    <p>The strength of the rebound in the Chinese economy of course has taken everyone by surprise. This has been helped by the Chinese government increasing support to the construction industry issuing substantial bonds of 2.6trillion Yuan (circa £300bn) in local and central government bonds in order to do so.</p>
    <p>Fitch forecasts China will see flat growth in copper consumption in 2020 which is a great improvement over the fears of a contraction due to Covid-19, especially in light of the falling supply and so should help tighten the market due to supply and demand mechanics</p>
    <p>Even before the current crisis it was thought that demand for copper would increase due to the requirements in the manufacture of electric cars and renewable energy generation. Before the crisis McKinsey was forecasting demand could increase by up to 43% in the next 15 years!</p>
    <p>There are now musings from and external calls on various governments that they should take the opportunity of the current economic downturn to stimulate their economies through further investment in both these areas. Therefore as these countries also come out of lockdown this could boost demand even further in the coming months, squeezing the copper price to even greater heights.</p>
    <h2>Opportunity</h2>
    <p>Currently I am back in profit now that price has returned above that of my previous article and will continue to hold, closely monitoring how the market is behaving in the coming weeks to ensure supply and demand mechanics continue to play out as I hope. I invested via an exchange traded fund (“ETF”) called WisdomTree Copper as described in the how to trade box below. There are however several other ETF products available from WisdomTree, not just a pure replication of the copper price:</p>
